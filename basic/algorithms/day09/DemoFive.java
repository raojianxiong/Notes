package algo.day09;

import java.util.Random;

/**
 * 假设函数f()等概率随机返回一个在[0,1)范围上的浮点数，那么我们知道，
 * 在[0,x)区间上的数出现的概率为x(0<x<=1)。
 * 给定一个大于0的整数k,并且可以使用f()函数，
 * 请实现一个函数依然返回在[0,1)范围上的数，但是在[0,x)区间上的数出现的概率为x的k次方。
 * @author Jianxiong Rao
 *
 */
public class DemoFive {

	public static double random(int k) {
		double result = -1D;
		while(k> 0) {
			k --;
			double b = f();
			result = (result > b) ? result:b;
		}
		return result;
	}
	/**
	 * 随机返回在0 - 1 范围的浮点数
	 * @return
	 */
	private static double f() {
		return new Random(1).nextDouble();
	}
}
